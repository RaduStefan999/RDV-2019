                <div class="separator"><span style="font-size: 16px;">&#10013;</span></div>

                <h5 class="description text-justify">

                    &emsp; Libertatea absolută pentru om nu există prin fire ci prin prin participarea la libertatea
                    dumnezeiască. Condiția participării la această libertate este comuniunea cu Dumnezeu care se
                    realizează prin repectarea poruncilor. În caz contrar, prin încălcarea poruncilor omul rupe
                    comuniunea cu Dumnezeu și se supune stricăciunii (viciului) și morții. Prin comiterea
                    păcatului omul pierde harul Sfântului Duh care-l face părtaș de libertatea dumnezeiască. El
                    încetează să-L vadă pe Dumnezeu ca Tatăl Ceresc și își făurește dumnezei care îl stăpânesc
                    (alcoolul, tutunul, banii, faima, puterea etc). Abaterea către păcat este supunerea față de
                    stricăciune și moarte, iar în măsura în care trăiește unit cu Hristos se izbăvește de frica morții
                    și e liber.
                    <br><br>
                    &emsp; Libertatea față de lume se poate obține prin autoîngrădiri de bunăvoie și prin
                    conformare cu voia lui Dumnezeu. Prin aceste metode el își biruie patimile care îl înrobesc și
                    îl distrug. Totodată astfel câștigă și libertatea duhovnicească. Biruindu-și patimile și refăcând
                    stăpânirea minții asupra ființei lui, omul face cu putină manifestarea lucrărilor Duhului Sfânt.

