                <h2 class="title">II.<p>1</p> LIBERTATEA ȘI TIPURILE EI </h2>

                <div class="separator"><span style="font-size: 16px;">&#10013;</span></div>

                <h5 class="description text-justify">

                    I. După sensurile ei, libertatea poate fi:
                    <br>&emsp; &emsp;1. politică
                    <br>&emsp; &emsp;2. economică
                    <br>&emsp; &emsp;3. de sine însuși
                    <br>&emsp; &emsp;4. libertatea față de lume
                    <br><br> II. Din punct de vedere moral, libertatea este:
                    <br>&emsp; &emsp;1. mărginită și relativă (libertatea vieții cotidiene)
                    <br>&emsp; &emsp;2. nemărginită și absolută ( libertatea ontologică a Duhului)
                    <br/><br/>&emsp; Omul este o ființă creată, deci nu este liber prin fiea sa deoarece nu el este pricina
                    ființării lui. Altcineva l-a creat pe om. De aceea, despre om se spune că are existența
                    „împrumutată”. Asemenea existenței și libertatea îi este dată: omul are libertatea alegerii și nu
                    libertatea absolutei autodeteminări. Doar Dumnezeu are libertatea absolută fiindcă aceasta
                    înseamnă a hotărâ ea însăși modul existenței ei, în afara oricărei dependențe, necesități și
                    îngrădiri.
                    <br/><br/>&emsp; Omul nu are libertate absolută, dar libertatea lui mărginită este hotărâtoare pentru
                    apropierea de cea absolută. Ascultarea de Dumnezeu este consimțirea cu libertatea.
                    Înrobirea de bunăvoie de Dumnezeu înfăptuită prin ascultarea de voia Lui îl eliberează pe om
                    de păcat și de moarte și îl face părtaș nestricăciunii și nemuririi. În felul acesta omul accede la
                    libertatea absolută, la libertatea adevărată, necreată și dumnezeiască.

                </h5>
                <br/><br/><br/><br/><br/><br/>

